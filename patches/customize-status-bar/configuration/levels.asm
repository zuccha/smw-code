; greater than zero, respectively. 11 (3) means use the global setting.
; - LL: Life counter. 00 (0) and 01 (1) are never visible and always visible
; respectively. 11 (3) means use the global setting. 10 (2) is unused.
; - BB: Bonus stars counter. 00 (0) and 01 (1) are never visible and always
; visible respectively. 11 (3) means use the global setting. 10 (2) is unused.
; - TT: Time counter. 00 (0), 01 (1), and 10 (2) are never visible, always
